/**
 * Normalize language value from various sources to canonical 'id' or 'en'.
 */
export function normalizeLanguage(lang) {
  if (!lang) return 'id';
  const lower = String(lang).toLowerCase().trim();
  if (lower === 'en' || lower === 'english') return 'en';
  return 'id'; // 'id', 'indonesia', or any unknown → fallback to Indonesian
}

/**
 * Create a translator function for the given language.
 * Returns a function t(key) that looks up the translation.
 */
export function createTranslator(language) {
  const lang = normalizeLanguage(language);
  const dict = translations[lang] || translations.id;

  return function t(key) {
    return dict[key] ?? translations.id[key] ?? key;
  };
}

